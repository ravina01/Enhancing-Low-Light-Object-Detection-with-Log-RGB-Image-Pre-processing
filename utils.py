# pred_bbox is scaled down by factor of width and height factor so getting it back to regular xyxy format
def scale_single_bbox(bbox, width_scale_factor, height_scale_factor, mode='a2p'):
    assert mode in ['a2p', 'p2a']
    
    proj_bbox = bbox.clone()
    
    if mode == 'a2p':
        # activation map to pixel image
        proj_bbox[[0, 2]] *= width_scale_factor
        proj_bbox[[1, 3]] *= height_scale_factor
    else:
        # pixel image to activation map
        proj_bbox[[0, 2]] /= width_scale_factor
        proj_bbox[[1, 3]] /= height_scale_factor
    
    return proj_bbox

